package corejava;

public  abstract  class Animals 
{
	//variable declaration
	protected int age;
 
	
	//create a parameterized constructor for age
	public Animals(int age)
	{
		super();
		this.age = age;
	}
	
	//creating  getters &setters for age
	public int getAge() 
	{
		return age;
	}
	public void setAge(int age)
	{
		this.age = age;
	}
	
	


	

	

}
