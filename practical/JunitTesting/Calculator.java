package JunitTesting;

public class Calculator {
	public static  int add(int num1 , int nnum2) {
		return num1+nnum2;
	}
	
	public static  int subtract(int num1 , int nnum2) {
		return num1-nnum2;
	}
	
	public static  int multiply(int num1 , int nnum2) {
		return num1*nnum2;
	}
	
	public static  int division(int num1 , int nnum2) {
		return num1/nnum2;
	}
	
}
